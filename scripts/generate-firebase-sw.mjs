import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const rootDir = path.join(__dirname, "..");
const outputPath = path.join(rootDir, "public", "firebase-messaging-sw.js");
const envPath = path.join(rootDir, ".env");

function loadEnvFile() {
  if (!fs.existsSync(envPath)) return;

  const content = fs.readFileSync(envPath, "utf8");
  for (const line of content.split("\n")) {
    const trimmed = line.trim();
    if (!trimmed || trimmed.startsWith("#")) continue;

    const separatorIndex = trimmed.indexOf("=");
    if (separatorIndex === -1) continue;

    const key = trimmed.slice(0, separatorIndex).trim();
    const value = trimmed.slice(separatorIndex + 1).trim();

    if (!process.env[key]) {
      process.env[key] = value;
    }
  }
}

loadEnvFile();

const firebaseVersion = "11.6.0";

const config = {
  apiKey: process.env.NEXT_PUBLIC_FIREBASE_API_KEY || "",
  authDomain: process.env.NEXT_PUBLIC_FIREBASE_AUTH_DOMAIN || "",
  projectId: process.env.NEXT_PUBLIC_FIREBASE_PROJECT_ID || "",
  storageBucket: process.env.NEXT_PUBLIC_FIREBASE_STORAGE_BUCKET || "",
  messagingSenderId: process.env.NEXT_PUBLIC_FIREBASE_MESSAGING_SENDER_ID || "",
  appId: process.env.NEXT_PUBLIC_FIREBASE_APP_ID || "",
};

if (process.env.NEXT_PUBLIC_FIREBASE_MEASUREMENT_ID) {
  config.measurementId = process.env.NEXT_PUBLIC_FIREBASE_MEASUREMENT_ID;
}

const isConfigured = Boolean(config.apiKey && config.projectId && config.messagingSenderId);

const content = isConfigured
  ? `/* eslint-disable no-undef */
/* Generated by scripts/generate-firebase-sw.mjs — do not edit manually */
importScripts("https://www.gstatic.com/firebasejs/${firebaseVersion}/firebase-app-compat.js");
importScripts("https://www.gstatic.com/firebasejs/${firebaseVersion}/firebase-messaging-compat.js");

firebase.initializeApp(${JSON.stringify(config, null, 2)});

const messaging = firebase.messaging();

self.addEventListener("install", () => {
  self.skipWaiting();
});

self.addEventListener("activate", (event) => {
  event.waitUntil(self.clients.claim());
});

const FCM_CHANNEL_NAME = "aqdi-fcm";

async function forwardPayloadToClients(payload) {
  const windowClients = await clients.matchAll({
    type: "window",
    includeUncontrolled: true,
  });

  try {
    const channel = new BroadcastChannel(FCM_CHANNEL_NAME);
    channel.postMessage(payload);
    channel.close();
  } catch (error) {
    // BroadcastChannel may be unavailable in some contexts.
  }

  for (const client of windowClients) {
    client.postMessage({ type: "FCM_PUSH", payload });
  }

  return windowClients;
}

async function showSystemNotification(payload) {
  const notification = payload.notification || {};
  const data = payload.data || {};
  const title = notification.title || data.title || "إشعار جديد";
  const body = notification.body || data.body || "";
  const tag =
    data.type && data.order_id ? data.type + "-" + data.order_id : "aqdi-notification";

  await self.registration.showNotification(title, {
    body,
    icon: "/images/blue-logo.svg",
    badge: "/images/blue-logo.svg",
    data: Object.assign({}, data, {
      title,
      body,
    }),
    tag,
    dir: "rtl",
    lang: "ar",
  });
}

self.addEventListener("push", (event) => {
  if (!event.data) return;

  event.waitUntil(
    (async () => {
      let payload = null;

      try {
        payload = event.data.json();
      } catch (error) {
        return;
      }

      const windowClients = await forwardPayloadToClients(payload);
      const hasVisibleClient = windowClients.some(
        (client) => client.visibilityState === "visible"
      );

      if (hasVisibleClient) return;

      await showSystemNotification(payload);
    })()
  );
});

messaging.onBackgroundMessage(async (payload) => {
  const windowClients = await forwardPayloadToClients(payload);
  const hasVisibleClient = windowClients.some(
    (client) => client.visibilityState === "visible"
  );

  if (hasVisibleClient) return;

  await showSystemNotification(payload);
});

self.addEventListener("notificationclick", (event) => {
  event.notification.close();

  const data = event.notification.data || {};
  const orderId = data.order_id || data.orderId || data.contract_id;
  const type = String(data.type || data.notification_type || "").toLowerCase();

  let targetUrl = "/home";

  if (orderId) {
    targetUrl = "/home/orders/" + orderId;
  } else if (type.includes("order") || type.includes("notification")) {
    targetUrl = "/home/orders";
  }

  event.waitUntil(
    clients.matchAll({ type: "window", includeUncontrolled: true }).then((windowClients) => {
      for (const client of windowClients) {
        if ("focus" in client) {
          client.postMessage({ type: "FCM_NOTIFICATION_CLICK", data, targetUrl });
          return client.focus();
        }
      }

      if (clients.openWindow) {
        return clients.openWindow(targetUrl);
      }

      return undefined;
    })
  );
});
`
  : `/* Firebase is not configured. Set NEXT_PUBLIC_FIREBASE_* env vars and run npm run generate:firebase-sw */
self.addEventListener("install", () => {
  self.skipWaiting();
});

self.addEventListener("activate", (event) => {
  event.waitUntil(self.clients.claim());
});
`;

fs.writeFileSync(outputPath, content, "utf8");
console.log(
  isConfigured
    ? "[firebase-sw] Generated public/firebase-messaging-sw.js"
    : "[firebase-sw] Generated placeholder service worker (Firebase env vars missing)"
);
